package Lab30321;

public class BitwiseOperatorsDemo {
    public static void main(String[] args) {
        int x = 58;
        int y = 13;
        System.out.println("x & y : " + (x & y));
        System.out.println("x | y : " + (x | y));
        System.out.println("x ^ y : " + (x ^ y));
        System.out.println("~x : " + (~x));
        System.out.println("x << y : " + (x << y));
        System.out.println("x >> y : " + (x >> y));

    }
}
