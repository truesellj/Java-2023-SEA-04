package CoreJavaVariable;

public class AddIntegers {
    public static void main(String[] args) {
        int firstNum = 6;
        int secondNum = 3;
        int product = firstNum + secondNum;
        System.out.println("When you add " + Integer.toString(firstNum) + " + " + Integer.toString(secondNum) + ", you get " + Integer.toString(product));
    }
}
