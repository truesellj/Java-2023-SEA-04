package CoreJavaVariable;

public class AddDoubles {
    public static void main(String[] args) {
        double firstDec = 23.5;
        double secondDec = 10.7;
        double product = firstDec + secondDec;
        System.out.println("When you add " + Double.toString(firstDec) + " with " + Double.toString(secondDec) + ", you will get " + Double.toString(product));
    }
}
